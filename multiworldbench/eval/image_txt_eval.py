DEVICE = "cpu"
CLIP_MODEL_NAME = "ViT-B/32"


# ======================
# Load CLIP
# ======================
print("Loading CLIP model...")
clip_model, clip_preprocess = clip.load(CLIP_MODEL_NAME, device=DEVICE)
clip_model.eval()


# ======================
# Encoding utilities
# ======================
@torch.no_grad()
def encode_image(image_path: str) -> torch.Tensor:
    """
    Encode an image into a normalized CLIP feature.
    Returns: (D,)
    """
    img = Image.open(image_path).convert("RGB")
    img_input = clip_preprocess(img).unsqueeze(0).to(DEVICE)
    feat = clip_model.encode_image(img_input)
    feat = feat / feat.norm(dim=-1, keepdim=True)
    return feat.squeeze(0)


@torch.no_grad()
def encode_text(text: str) -> torch.Tensor:
    """
    Encode a text string into a normalized CLIP feature.
    Returns: (D,)
    """
    tokens = clip.tokenize([text], truncate=True).to(DEVICE)
    feat = clip_model.encode_text(tokens)
    feat = feat / feat.norm(dim=-1, keepdim=True)
    return feat.squeeze(0)


def cosine_similarity(f1: torch.Tensor, f2: torch.Tensor) -> float:
    """
    Cosine similarity between two normalized vectors.
    """
    return torch.dot(f1, f2).item()


# ======================
# Directory-level evaluation
# ======================
def evaluate_clip_image_text_dirs(
    image_dir: str,
    txt_dir: str,
    image_exts=(".jpg", ".jpeg", ".png"),
):
    """
    Compute CLIP image-text similarity for directory pairs.

    Returns:
        {
            "num_samples": int,
            "mean_score": float,
            "std_score": float,
            "scores": List[float],
            "per_sample": Dict[str, float]
        }
    """
    scores = []
    per_sample_scores = {}

    image_files = [
        f for f in os.listdir(image_dir)
        if f.lower().endswith(image_exts)
    ]
    image_files.sort()

    for img_file in tqdm(image_files, desc="Evaluating CLIP image-text"):
        stem = os.path.splitext(img_file)[0]
        img_path = os.path.join(image_dir, img_file)
        txt_path = os.path.join(txt_dir, stem + ".txt")

        if not os.path.exists(txt_path):
            print(f"[Warning] Missing text for {img_file}, skipped.")
            continue

        try:
            with open(txt_path, "r", encoding="utf-8") as f:
                text = f.read().strip()

            if len(text) == 0:
                print(f"[Warning] Empty text for {stem}, skipped.")
                continue

            img_feat = encode_image(img_path)
            text_feat = encode_text(text)
            score = cosine_similarity(img_feat, text_feat)

            scores.append(score)
            per_sample_scores[stem] = score

        except Exception as e:
            print(f"[Error] Failed on {stem}: {e}")

    if len(scores) == 0:
        raise RuntimeError("No valid image-text pairs evaluated.")

    return {
        "num_samples": len(scores),
        "mean_score": float(np.mean(scores)),
        "std_score": float(np.std(scores)),
        "scores": scores,
        "per_sample": per_sample_scores,
    }



def build_gpt_image_text_prompt() -> str:
    return """
You are an expert evaluator for image understanding and world-state description.

You will be given TWO inputs:
- ONE image, which represents the TRUE world state.
- ONE text description, which is GENERATED by a model to describe or predict the world state.

Your task is to evaluate how well the GENERATED text matches the image,
by considering both semantic correctness and description quality.

Please focus on the following aspects (in order of importance):

1. World-state correctness:
   - Does the text correctly describe the key objects, entities, and their states in the image?
   - Are object attributes (positions, relations, conditions) consistent with what is visible?
   - If the text describes a different or incorrect world state, the score should be LOW,
     even if the text is fluent.

2. Completeness and coverage:
   - Does the text cover the important aspects of the scene?
   - Are any critical objects or states missing or misrepresented?

3. Internal consistency:
   - Is the text logically consistent and free of contradictions?

4. Language quality:
   - Is the text clear, coherent, and well-formed?

Important rules:
- Judge correctness over fluency.
- Do NOT reward text that sounds plausible but does not match the image.
- Do NOT hallucinate objects or states that are not visible in the image.

Give a single numeric score from 0 to 10:
- 10: The text accurately and completely describes the image world state.
- 7–9: Mostly correct with minor omissions or inaccuracies.
- 4–6: Partially correct but missing or misrepresenting key elements.
- 1–3: Largely incorrect or inconsistent with the image.
- 0: Completely wrong or unrelated.

Only output the number. Do not explain.
"""

token = os.environ.get("MWB_EVAL_API_KEY", "")

def _require_token(value: str, env_name: str = "MWB_EVAL_API_KEY") -> str:
    if not value:
        raise RuntimeError(f"Missing evaluator API token. Please set {env_name}.")
    return value

def _require_endpoint(value: str, env_name: str = "MWB_EVAL_API_URL") -> str:
    if not value:
        raise RuntimeError(f"Missing evaluator API endpoint. Please set {env_name}.")
    return value

def encode_base64(image_path: str) -> str:
    """
    Encode an image file (given by path) to a base64 string.
    """
    with open(image_path, "rb") as _f:
        data = _f.read()
    return base64.b64encode(data).decode()

def llm(prompt: str,model: str = "gpt-5.1",image_path: Optional[Union[str, List[str]]] = None,max_tokens: int = 1688,temperature: float = 0.5
):
    url = _require_endpoint(os.environ.get("MWB_EVAL_API_URL", ""))

    content = [{"type": "text", "text": prompt}]

    if image_path is not None:
        if isinstance(image_path, str):
            image_path = [image_path]

        for img_path in image_path:
            image_base64 = encode_base64(img_path)
            content.append({
                "type": "image_url",
                "image_url": {
                    "url": f"data:image/png;base64,{image_base64}"
                }
            })

    payload = json.dumps({
        "model": model,
        "messages": [
            {
                "role": "user",
                "content": content
            }
        ],
        "max_tokens": max_tokens,
        "temperature": temperature
    })

    headers = {
        "Authorization": f"Bearer {_require_token(token)}",
        "Content-Type": "application/json"
    }

    response = requests.post(url, headers=headers, data=payload)
    response.raise_for_status()

    result = response.json()["choices"][0]["message"]["content"]
    return result

def compute_gptscore_image_text(
    image_path: str,
    text: str,
    model: str = "gpt-5.1-2025-11-13"
) -> float:
    """
    Compute GPTScore for one (image, text) pair.
    Score range: [0, 10]
    """
    prompt = build_gpt_image_text_prompt()

    # 把 text 直接拼进 prompt（而不是作为第二条 message）
    full_prompt = prompt + "\n\nGenerated text description:\n" + text.strip()

    result = llm(
        prompt=full_prompt,
        model=model,
        image_path=image_path
    )

    try:
        score = float(result.strip())
    except ValueError:
        raise RuntimeError(f"Invalid GPT output: {result}")

    return score


